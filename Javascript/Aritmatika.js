var a = 5;

// Penambahan dengan assignment
a += 3; // Sama dengan a = a + 3
console.log(a); // Output: 8

// Pengurangan dengan assignment
a -= 2; // Sama dengan a = a - 2
console.log(a); // Output: 6

// Perkalian dengan assignment
a *= 4; // Sama dengan a = a * 4
console.log(a); // Output: 24

// Pembagian dengan assignment
a /= 3; // Sama dengan a = a / 3
console.log(a); // Output: 8

